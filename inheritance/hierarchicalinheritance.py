"""hierachical inheritance"""
class details:
    def __init__(self,sid,name,gender):
        self.sid=sid
        self.name=name
        self.gender=gender
    def getdata(self):
        print("sid:",self.sid)
        print("name:",self.name)
        print("gender:",self.gender)
class employee(details):
    def __init__(self,sid,name,gender,company_name,department):
        details.__init__(self,sid,name,gender)
        self.company_name=company_name
        self.department=department
    def getemp(self):
        details.getdata(self)
        print("company name:",self.company_name)
        print("department: ",self.department)
class doctor(details):
    def __init__(self,sid,name,gender,hospital_name,department):
        details.__init__(self,sid,name,gender)
        self.hospital_name=hospital_name
        self.department=department
    def getdoc(self):
        details.getdata(self)
        print("hospital name:",self.hospital_name)
        print("department: ",self.department)
class teacher(details):
    def __init__(self,sid,name,gender,school_name,subject):
        details.__init__(self,sid,name,gender)
        self.school_name=school_name
        self.subject=subject
    def gettec(self):
        details.getdata(self)
        print("school name:",self.school_name)
        print("subject: ",self.subject)

